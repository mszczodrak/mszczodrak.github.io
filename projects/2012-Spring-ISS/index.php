<h2>Industrial Sensor Systems - Spring 2012</h2>

<p>
Most parts of India are largely dependent on the
four month long, seasonal rainfall for water. Every
year some of these parts face water shortage, some
parts even face drought. Thus, water is a precious
resource. There is a need for monitoring water levels
at water storage and distribution facilities, primarily 
to check for the wastage and misuse of water
in the form of overflow and water pilferage, respectively.
</p>

<p>
Traditionally, sensors monitoring water levels 
require lengthy cables to be laid down from the sensor 
location(in the water tank) to the monitoring
station. Cables of length 1 mile could also be laid
down, which could contribute to about half of the
cost of the total deployment, including the cost of
the sensors.
</p>

<p>
Considering that wireless motes are available at
the cost of $20 each, an alternative deployment of
a wireless sensor network would eliminate the need
for cables and reduce costs significantly.
The same could be applied to Process Indus-
tries, where sensors measuring level, temperature,
pressure and other physical process parameters are
present in large numbers. Thus, installation of wireless 
motes in such industries could significantly reduce the setup cost.
</p>

