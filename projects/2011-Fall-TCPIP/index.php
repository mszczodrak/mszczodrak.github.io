<h2>TCP/IP for Low Power Wireless Sensor Networks - Fall 2011</h2>

<p>
Low Power Wireless Networks (LPWN) consists of small, battery powered 
devices with limited computation, memory and communication resources, 
with attached sensors and actuators. These networks are applied to many 
disciplines: health-care, military, environment monitoring, smart buildings 
and smart cities, just to name few. 
</p>

<p>
Currently researchers debate how to implement communication for LPWN. 
On one side there are various approaches proposing new communication 
protocols, such as CTP or Flush. On the other side, there are attempts of 
using standard TCP/IP approach with IPv6 addressing. In this project we 
design a network operating in two modes: standard IP and non-standard using 
ad-hoc protocols. We will study related literature of both approaches. Next, 
we will implement and evaluate TCP/IP and non-IP network communication 
infrastructures and demonstrate complete system functionality. 
</p>

